    private void JBsavActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBsavActionPerformed
        // TODO add your handling code here:
        if(newdata){
            try {
                String sql ="INSERT INTO PassMan VALUES ('%s','%s','%s','%s','%s')";
                
                sql = String.format(
                        sql,
                        JT1.getText(),
                        JT2.getText(),
                        JT3.getText(),
                        JT4.getText(),
                        JT5.getText()
                        
                );
                
                Connection cn = koneksi.getKoneksi();
                PreparedStatement pst = cn.prepareStatement(sql);
                
                pst.execute();
                JOptionPane.showMessageDialog(null, "Berhasil Ditambahkan");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Ditambahkan"+ ex);
                Logger.getLogger(PasswordManager.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        }else{
            try{
                String sql ="UPDATE PassMan SET Application='%s',Username='%s',Password='%s',Description='%s' WHERE Account_Name='%s'";
                sql = String.format(
                        sql,
                        
                        JT2.getText(),
                        JT3.getText(),
                        JT4.getText(),
                        JT5.getText(),
                        JT1.getText()
                        
                );
                Connection cn = koneksi.getKoneksi();
                PreparedStatement pst = cn.prepareStatement(sql);
                
                pst.execute();
                JOptionPane.showMessageDialog(null, "Berhasil Update");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Update"+ ex);
                Logger.getLogger(PasswordManager.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        }
        getData();
    }//GEN-LAST:event_JBsavActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            // TODO add your handling code here:
            newdata = false;
            
            int row = tabel.getSelectedRow();
            String Account_Name = tabel.getModel().getValueAt(row, 0).toString();
            
            Connection cn = koneksi.getKoneksi();
            Statement stm = cn.createStatement();
            ResultSet st = stm.executeQuery("SELECT * FROM PassMan WHERE Account_Name='"+Account_Name+"'");
            
            if(st.next()){
                JT1.setText(st.getString("Account_Name"));
                JT2.setText(st.getString("Application"));
                JT3.setText(st.getString("Username"));
                JT4.setText(st.getString("Password"));
                JT5.setText(st.getString("Description"));
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(PasswordManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabelMouseClicked

