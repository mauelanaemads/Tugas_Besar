    private void getData(){
        try{
            Connection cn = koneksi.getKoneksi();
            Statement stm = cn.createStatement();
            ResultSet st = stm.executeQuery("SELECT * FROM PassMan");
            
            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            
            model.setRowCount(0);
            while(st.next()){
                String Account_Name = st.getString("Account_Name");
                String Application = st.getString("Application");
                String Username = st.getString("Username");
                String Password = st.getString("Password");
                String Description = st.getString("Description");
                
                model.addRow(new Object[]{Account_Name,Application,Username,Password,Description});
                
            }
    }   catch (SQLException ex) {
            Logger.getLogger(PasswordManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
